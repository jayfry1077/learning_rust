fn hmm() -> i32 {
    5
}

fn main() {
    // addition
    let sum = 5 + 10;

    // subtraction
    let difference = 95.5 - 4.3;

    // multiplication
    let product = 4 * 30;

    // division
    let quotient = 56.7 / 32.2;
    let floored = 2 / 3; // Results in 0

    // remainder
    let remainder = 43 % 5;

    let ahh = hmm();

    println!("{} {} {} {} {} {} {}",sum, difference, product, quotient, floored, remainder, ahh)
}
